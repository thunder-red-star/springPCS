public class Cereal {
    public String name;
    public String type;
    public int calories;
    public double protein;
    public double fat;
    public double sodium;
    public double fiber;
    public double sugar;
    public double potassium;
    public double vitamins;
    public double shelf;
    public double weight;
    public double cups;
    public double price;

    public Cereal (String name, String type, int calories, double protein, double fat, double sodium, double fiber, double sugar, double potassium, double vitamins, double shelf, double weight, double cups, double price) {
	this.name = name;
	this.type = type;
	this.calories = calories;
	this.protein = protein;
	this.fat = fat;
	this.sodium = sodium;
	this.fiber = fiber;
	this.sugar = sugar;
	this.potassium = potassium;
	this.vitamins = vitamins;
	this.shelf = shelf;
	this.weight = weight;
	this.cups = cups;
	this.price = price;
    }
}
